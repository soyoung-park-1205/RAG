def build_context_prompt(context, question):
    return f"""
    당신은 뉴스 기사를 참고하여 사용자의 질문에 답변하는 AI입니다.

    다음은 최근의 '{question}' 관련 뉴스 기사 내용입니다:
    
    기사 내용: 
    {context}
    
    질문: {question}

    아래 내용을 반드시 지켜서 답변해 주세요.
    (1) 답변은 반드시 위의 기사 내용 만을 활용할 것. (기사에 언급된 명사 만을 활용하여 답변)
    (2) 사용자와 대화 하듯이 간결하게 답변할 것.
    (3) "내가 대답한다면, ", "이렇게 답변해보세요." 등의 표현은 절대 사용하지 말 것.
    """
